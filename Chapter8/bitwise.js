//bitwise AND
console.log(5&1);                       //1

//bitwise or
console.log(5|1);                       //5

//bitwise XOR
console.log(5^1);                       //4

//bitwise NOT
console.log(~5);                        //-6

//bitwise Zero fill left shift
console.log(5<<1);                      //10

//bitwise Signed right shift
console.log(5>>1);                      //2

//bitwise Zero fill right shift
console.log(5>>>1);                     //2
